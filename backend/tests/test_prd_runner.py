"""Tests for app.prd_runner — the SPLIT PRD generation.

The human PRD (Part A) and the machine-readable Implementation Spec (Part B) are
now generated by two SEPARATE flows:

  - `generate_prd` / `_run_sync` generate ONLY the human PRD via the `prd-author`
    skill and store it in payload_md. They do NOT produce or trigger Part B.
  - `ensure_impl_spec` generates Part B ON DEMAND via the `implementation-spec`
    skill (fed the finished human PRD), caches it in `llm_part` keyed to the
    human PRD's content hash, and is idempotent: a re-send reuses the cache;
    editing the human PRD invalidates it so the next send regenerates.

These tests mock at the gateway/llm seam: most patch `prd_runner.llm_call`; a
couple let the REAL gateway run and patch `app.llm.call_md` to assert the
METHOD (prd-author for the PRD, implementation-spec for the spec) reaches the
prompt.

New rows are written with variant='v2' by the route; the runner itself
produces the human PRD and calls `complete_prd` against the existing row.
"""
from __future__ import annotations

import asyncio

import pytest

from app import prd_runner
from app.graph.gateway import LLMResult


def _seed_corpus(data_dir, dataset="asurion", body="corpus body"):
    ds = data_dir / dataset
    ds.mkdir(exist_ok=True)
    (ds / "a.md").write_text(body)


def _seed_brief(db_mod, dataset="asurion", insights=None):
    if insights is None:
        insights = [{"title": "Insight A", "subtitle": "behaviour"}]
    payload = {
        "summary_headline": "stub",
        "insights": insights,
        "_schema_version": 1,
    }
    return db_mod.save_brief(
        dataset=dataset, week_label="Week of stub", payload=payload, schema_version=1
    )


def _llm_result(output, model="claude-sonnet-4-6", prompt_version="prd-author-v2"):
    return LLMResult(
        output=output, model=model, prompt_version=prompt_version,
        input_tokens=10, output_tokens=5, cache_read_input_tokens=0,
        cache_creation_input_tokens=0, cost_usd=0.001, latency_ms=5,
        stop_reason="end_turn",
    )


_PART_A = (
    "# Surface — Ship the thing\n\n"
    "# Part A — Product Requirements Document (human-readable)\n"
    "## 1. Problem & evidence\nUsers can't X.\n"
)
_PART_B = (
    "# Implementation Spec\n"
    "## B0. Available artifacts\nWHEN x THE SYSTEM SHALL y.\n"
)


def _part_a_mock(part_a=_PART_A, captured=None):
    """A `llm_call` stub that records each call's kwargs and returns Part A.

    Records the call so a test can assert the human-PRD flow makes exactly ONE
    call and NEVER a Part-B (implementation-spec) call.
    """
    captured = [] if captured is None else captured

    def _call(**kwargs):
        captured.append(kwargs)
        return _llm_result(part_a)

    return _call, captured


def _start_prd(db_mod, brief_id, title="t", insight_index=0):
    return db_mod.start_prd(
        brief_id=brief_id, insight_index=insight_index, title=title,
        template_version=1, variant="v2",
    )


