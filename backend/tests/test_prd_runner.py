def test_ensure_impl_spec_generates_via_implementation_spec_skill(
    isolated_settings, monkeypatch
):
    """First send: Part B is generated by the implementation-spec skill, fed the
    finished human PRD, and stored in llm_part."""
    db_mod, prd_id = _ready_human_prd(isolated_settings, monkeypatch)

    captured: list = []

    def _spec_call(**kwargs):
        captured.append(kwargs)
        return _llm_result(_PART_B, prompt_version="prd-impl-spec-v1")

    monkeypatch.setattr(prd_runner, "llm_call", _spec_call)
    result = prd_runner.ensure_impl_spec(prd_id)

    assert result["cached"] is False
    assert "WHEN x THE SYSTEM SHALL y." in result["llm_part"]
    # Exactly one implementation-spec call, fed the finished human PRD.
    assert len(captured) == 1
    assert captured[0]["skill"] == "implementation-spec"
    assert captured[0]["purpose"] == "generate_prd_part_b"
    assert "PART A — HUMAN PRD" in captured[0]["input"]
    assert "Ship the thing" in captured[0]["input"]  # human PRD text flows in
    # Persisted to llm_part keyed to the human PRD hash.
    row = db_mod.get_prd(prd_id)
    assert "Implementation Spec" in row["llm_part"]
    assert row["llm_part_source_hash"]


def test_ensure_impl_spec_caches_second_send_reuses(isolated_settings, monkeypatch):
    """Second send with an unchanged human PRD reuses the cache — NO second
    implementation-spec call."""
    db_mod, prd_id = _ready_human_prd(isolated_settings, monkeypatch)

    calls = {"n": 0}

    def _spec_call(**kwargs):
        calls["n"] += 1
        return _llm_result(_PART_B, prompt_version="prd-impl-spec-v1")

    monkeypatch.setattr(prd_runner, "llm_call", _spec_call)

    first = prd_runner.ensure_impl_spec(prd_id)
    second = prd_runner.ensure_impl_spec(prd_id)

    assert first["cached"] is False
    assert second["cached"] is True
    assert second["llm_part"] == first["llm_part"]
    # The model was invoked exactly once across both sends.
    assert calls["n"] == 1


def test_editing_human_prd_invalidates_spec_cache(isolated_settings, monkeypatch):
    """Editing the human PRD invalidates the cached spec: the next send
    regenerates (cache miss) against the new text."""
    db_mod, prd_id = _ready_human_prd(isolated_settings, monkeypatch)

    outputs = ["# Spec v1\nWHEN a THE SYSTEM SHALL b.", "# Spec v2\nWHEN c THE SYSTEM SHALL d."]
    idx = {"i": 0}

    def _spec_call(**kwargs):
        out = outputs[idx["i"]]
        idx["i"] += 1
        return _llm_result(out, prompt_version="prd-impl-spec-v1")

    monkeypatch.setattr(prd_runner, "llm_call", _spec_call)

    first = prd_runner.ensure_impl_spec(prd_id)
    assert first["cached"] is False
    assert "Spec v1" in first["llm_part"]

    # Edit the human PRD — this clears llm_part + the source hash.
    from app.db.prds import update_prd_content
    update_prd_content(prd_id, title="t", payload_md="# Edited PRD\nbrand new body")
    row = db_mod.get_prd(prd_id)
    assert (row["llm_part"] or "") == ""
    assert (row.get("llm_part_source_hash") or "") == ""

    # Next send is a cache MISS and regenerates against the edited PRD.
    second = prd_runner.ensure_impl_spec(prd_id)
    assert second["cached"] is False
    assert "Spec v2" in second["llm_part"]
    # The spec call was fed the EDITED human PRD text.
    # (idx advanced twice → two distinct generations.)
    assert idx["i"] == 2


def test_changed_prd_text_alone_forces_regenerate(isolated_settings, monkeypatch):
    """Even without going through update_prd_content, a payload_md whose hash
    differs from the cached source_hash forces a regenerate (hash check is the
    source of truth)."""
    db_mod, prd_id = _ready_human_prd(isolated_settings, monkeypatch)

    monkeypatch.setattr(
        prd_runner, "llm_call",
        lambda **kw: _llm_result(_PART_B, prompt_version="prd-impl-spec-v1"),
    )
    prd_runner.ensure_impl_spec(prd_id)  # populate cache

    # Simulate a stale cache: keep llm_part but mutate payload_md directly.
    db_mod.get_prd(prd_id)
    isolated_settings["supabase"].table("prds").update(
        {"payload_md": "# Totally different PRD body"}
    ).eq("id", prd_id).execute()

    calls = {"n": 0}

    def _spec_call(**kwargs):
        calls["n"] += 1
        return _llm_result("# regenerated", prompt_version="prd-impl-spec-v1")

    monkeypatch.setattr(prd_runner, "llm_call", _spec_call)
    result = prd_runner.ensure_impl_spec(prd_id)
    assert result["cached"] is False
    assert calls["n"] == 1


